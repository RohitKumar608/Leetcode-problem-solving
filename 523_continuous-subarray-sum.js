/**
 * @param {number[]} nums
 * @param {number} k
 * @return {boolean}
 */
var checkSubarraySum = function (nums, k) {
  let total = 0
  let remainder = { 0: -1 }
  for (let i = 0; i < nums.length; i++) {
    total += nums[i]
    const mod = total % k
    if (remainder[mod] == undefined) {
      remainder[mod] = i
    } else if (i - remainder[mod] > 1) {
      return true
    }
  }
  return false
}
